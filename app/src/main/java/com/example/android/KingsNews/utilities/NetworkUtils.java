package com.example.android.KingsNews.utilities;

import android.net.Uri;

import java.io.IOException;
import java.io.InputStream;
import java.net.HttpURLConnection;
import java.net.MalformedURLException;
import java.net.URL;
import java.util.Scanner;

public class NetworkUtils {

    final static String NEWS_API_BASE_URL =
            "https://newsapi.org/v2/everything";

    final static String PARAM_QUERY = "q";

    /*
     * The sort field. One of stars, forks, or updated.
     * Default: results are sorted by best match if no field is specified.
     */
    final static String API_KEY = "apiKey";
    final static String key = "db99236a8ea242118a6d1ed17be31cf3";


    public static URL buildUrl(String newsSearchQuery) {
        Uri builtUri = Uri.parse(NEWS_API_BASE_URL).buildUpon()
                .appendQueryParameter(PARAM_QUERY, newsSearchQuery)
                .appendQueryParameter(API_KEY, key)
                .build();

        URL url = null;
        try {
            url = new URL(builtUri.toString());
        } catch (MalformedURLException e) {
            e.printStackTrace();
        }

        return url;
    }

    public static URL buildUrl(String newsSearchQuery,String Date) {
        Uri builtUri = Uri.parse(NEWS_API_BASE_URL).buildUpon()
                .appendQueryParameter(PARAM_QUERY, newsSearchQuery)
                .appendQueryParameter(API_KEY, key)
                .build();

        URL url = null;
        try {
            url = new URL(builtUri.toString());
        } catch (MalformedURLException e) {
            e.printStackTrace();
        }

        return url;
    }


    public static String getResponseFromHttpUrl(URL url) throws IOException {
        HttpURLConnection urlConnection = (HttpURLConnection) url.openConnection();
        try {
            InputStream in = urlConnection.getInputStream();

            Scanner scanner = new Scanner(in);
            scanner.useDelimiter("\\A");

            boolean hasInput = scanner.hasNext();
            if (hasInput) {
                return scanner.next();
            } else {
                return null;
            }
        } finally {
            urlConnection.disconnect();
        }
    }
}